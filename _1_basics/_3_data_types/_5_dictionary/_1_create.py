#  values can be of any data type and can repeat, keys must be of immutable type (string, number or tuple with immutable elements) and must be unique.

dict1 = {}
dict2 = {1: 'bawa', 2: 'vikram'}
dict3 = {'name': 'bawa', 1: [1,2,'bawa']}
dict4 = dict({1: 'rahul', 2: 'kumar'})
dict5 = dict([(1, 'apple'), (2, 'ball')])

print(dict1)
print(dict2)
print(dict3)
print(dict4)
print(dict5)
